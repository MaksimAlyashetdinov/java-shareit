DROP TABLE IF EXISTS users;
DROP TABLE IF EXISTS items;
DROP TABLE IF EXISTS bookings;
DROP TABLE IF EXISTS comments;

CREATE TABLE IF NOT EXISTS users (
    id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255),
    email VARCHAR(255)
);

CREATE TABLE IF NOT EXISTS items (
    id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    name VARCHAR(255),
    description VARCHAR(200),
    available BOOLEAN,
    owner_id BIGINT REFERENCES users(id) ON DELETE CASCADE ON UPDATE CASCADE,
    last_start_booking DATE,
    last_end_booking DATE,
    next_start_booking DATE,
    next_end_booking DATE
);

CREATE TABLE IF NOT EXISTS bookings (
    id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    item_id INT REFERENCES items(id) ON DELETE CASCADE ON UPDATE CASCADE,
    user_id INT REFERENCES users(id) ON DELETE CASCADE ON UPDATE CASCADE,
    start_booking DATE NOT NULL,
    end_booking DATE NOT NULL,
    state VARCHAR,
    create_date DATE
);

CREATE TABLE IF NOT EXISTS comments (
    id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    item_id INT REFERENCES items(id) ON DELETE CASCADE ON UPDATE CASCADE,
    user_id INT REFERENCES users(id) ON DELETE CASCADE ON UPDATE CASCADE,
    text VARCHAR,
    create_date DATE
);