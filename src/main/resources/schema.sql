DROP TABLE IF EXISTS users;
DROP TABLE IF EXISTS items;
DROP TABLE IF EXISTS bookings;

CREATE TABLE IF NOT EXISTS users (
    user_id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_name VARCHAR(255),
    email VARCHAR(255)
);

CREATE TABLE IF NOT EXISTS items (
    item_id BIGINT NOT NULL GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    item_name VARCHAR(255),
    description VARCHAR(200),
    available BOOLEAN,
    ownerId BIGINT REFERENCES users(user_id) ON DELETE CASCADE ON UPDATE CASCADE
);

CREATE TABLE IF NOT EXISTS bookings (
    item_Id INT REFERENCES items(item_id) ON DELETE CASCADE ON UPDATE CASCADE,
    user_id INT REFERENCES users(user_id) ON DELETE CASCADE ON UPDATE CASCADE,
    start_Booking DATE NOT NULL,
    end_Booking DATE NOT NULL
);